package com.sera.util;

public class DonemSonucRep {
	
    String deger;
    String donem;
    String baslik;
    String parentBaslik;
    Double sonuc;
    
    public DonemSonucRep(){
    	
    }
	public String getDonem() {
		return donem;
	}
	public void setDonem(String donem) {
		this.donem = donem;
	}
	public String getDeger() {
		return deger;
	}
	public void setDeger(String deger) {
		this.deger = deger;
	}
	public String getBaslik() {
		return baslik;
	}
	public void setBaslik(String baslik) {
		this.baslik = baslik;
	}
	public String getParentBaslik() {
		return parentBaslik;
	}
	public void setParentBaslik(String parentBaslik) {
		this.parentBaslik = parentBaslik;
	}
	public Double getSonuc() {
		return sonuc;
	}
	public void setSonuc(Double sonuc) {
		this.sonuc = sonuc;
	}
}
